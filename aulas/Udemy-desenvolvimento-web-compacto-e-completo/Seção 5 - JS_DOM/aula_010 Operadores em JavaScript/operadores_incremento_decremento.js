// OPERADORES DE INCREMENTO E DECREMENTO

let valor1 = 100
//pós incremento: apresenta o valor primeiro, antes da operação.
console.log(valor1++)       // 100
console.log(valor1)         // 101

//pós decremento: apresenta o valor primeiro, antes da operação.
console.log(valor1--)       // 100
console.log(valor1)         // 99

// pré incremento: apresenta o valor após operação
console.log(++valor1)       // 101
console.log(valor1)         // 101

// pré decremento: apresenta o valor após operação
console.log(--valor1)       // 99
console.log(valor1)         // 99
